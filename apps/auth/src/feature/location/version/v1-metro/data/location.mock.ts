// Generate 4700+ locations for large dataset testing
function generateLargeDataset(): Location[] {
  const cities = ['Ho Chi Minh City', 'Ha Noi City', 'Da Nang', 'Can Tho', 'Hai Phong'];
  const states = ['Ho Chi Minh', 'Ha Noi', 'Da Nang', 'Can Tho', 'Hai Phong'];
  const districts = ['Quận 1', 'Quận 2', 'Quận 3', 'Quận 4', 'Quận 5', 'Quận 7', 'Quận 9', 'Tân Bình', 'Tân Phú', 'Bình Thạnh'];
  const types = ['Store', 'Kitchen', 'Warehouse', 'Hub'];
  const locationTypeIds = [1, 2, 3, 4];
  const foodNames = ['Phở', 'Cà Phê', 'Cơm', 'Bánh Mì', 'Pizza', 'Burger', 'Gà Rán', 'Mì Ý', 'Sushi', 'Bánh Trung Thu'];

  const generated: Location[] = [];

  for (let i = 0; i < 4700; i++) {
    const foodName = foodNames[i % foodNames.length];
    const district = districts[i % districts.length];
    const city = cities[Math.floor(i / 1000) % cities.length];
    const state = states[Math.floor(i / 1000) % states.length];
    const type = types[Math.floor(i / 1175) % types.length];
    const typeId = locationTypeIds[Math.floor(i / 1175) % locationTypeIds.length];
    const isActive = i % 15 !== 0; // 93% active

    generated.push({
      id: `loc-${String(i + 1000).padStart(4, '0')}`,
      tenant_id: 'T001',
      node_id: `NODE-T001-${String(i + 1000).padStart(4, '0')}`,
      legacy_id: `LOC-${city.substring(0, 3).toUpperCase()}${String(i + 1).padStart(3, '0')}`,
      name: `${foodName} ${district} #${(i % 50) + 1}`,
      is_active: isActive,
      status_id: isActive ? 30001 : 30002,
      location_type_id: typeId,
      location_type_text: type,
      address_line_1: `${(i % 500) + 1}/${(i % 100) + 1} ${district}, ${city}`,
      city: city,
      state: state,
      postal_code: '70000',
      phone_number: `028 ${Math.floor(Math.random() * 9000) + 1000} ${String(i % 9999).padStart(4, '0')}`,
      email: `location${i + 1000}@zap.com`,
      created_at: now,
      updated_at: now,
    });
  }

  return generated;
}

export const EXTENDED_LOCATIONS = [...MOCK_LOCATIONS, ...generateLargeDataset()];

export const getMockLocations = (filter?: LocationFilter, page = 1, pageSize = 10) => {
  let filtered = [...EXTENDED_LOCATIONS];
  
  if (filter?.search) {
    let searchValue = typeof filter.search === 'string' ? filter.search : filter.search.value || '';
    if (searchValue) {
      const search = searchValue.toLowerCase();
      filtered = filtered.filter(l => 
        l.name.toLowerCase().includes(search) || 
        l.legacy_id.toLowerCase().includes(search) ||
        (l.address_line_1?.toLowerCase().includes(search) ?? false) ||
        (l.city?.toLowerCase().includes(search) ?? false)
      );
    }
  }

  if (filter?.filters?.is_active && filter.filters.is_active.length > 0) {
    filtered = filtered.filter(l => filter.filters?.is_active?.includes(l.is_active));
  }

  if (filter?.filters?.warehouse_type && filter.filters.warehouse_type.length > 0) {
    filtered = filtered.filter(l => l.location_type_text && filter.filters?.warehouse_type?.includes(l.location_type_text));
  }

  const totalRecord = filtered.length;
  const totalPage = Math.ceil(totalRecord / pageSize);
  const start = (page - 1) * pageSize;
  const items = filtered.slice(start, start + pageSize);

  return {
    success: true,
    code: 200,
    message: "Success",
    data: {
      total_page: totalPage,
      total_record: totalRecord,
      page_index: page,
      page_size: pageSize,
      items: items
    }
  };
};

export const getMockLocationById = (id: string) => {
  return EXTENDED_LOCATIONS.find(l => l.id === id) || null;
};

export const getMockLocationCities = () => {
  const cities = EXTENDED_LOCATIONS.map(l => l.city).filter(Boolean) as string[];
  return Array.from(new Set(cities));
};
