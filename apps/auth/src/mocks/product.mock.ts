export async function getMockProducts(filter?: ProductFilter, page = 1, pageSize = 10): Promise<ProductResponse> {
  await new Promise((resolve) => setTimeout(resolve, 300));
  let filtered = [...MOCK_PRODUCTS];

  if (filter) {
    if (filter.search) {
      const query = filter.search.toLowerCase();
      filtered = filtered.filter(
        (p) => p.name.toLowerCase().includes(query) || p.sku?.toLowerCase().includes(query)
      );
    }
  }

  const total = filtered.length;
  const totalPages = Math.ceil(total / pageSize);

  if (pageSize === 9999) {
    return {
      total_page: totalPages,
      total_record: total,
      page_index: page,
      page_size: pageSize,
      items: filtered,
    };
  }

  const start = (page - 1) * pageSize;
  const end = start + pageSize;
  const items = filtered.slice(start, end);

  return {
    total_page: totalPages,
    total_record: total,
    page_index: page,
    page_size: pageSize,
    items,
  };
}

export async function getMockProductById(id: string): Promise<Product | null> {
  await new Promise((resolve) => setTimeout(resolve, 100));
  return MOCK_PRODUCTS.find((p) => p.id === id) || null;
}

export async function getMockProductCategories(): Promise<string[]> {
  await new Promise((resolve) => setTimeout(resolve, 100));
  return Array.from(new Set(MOCK_PRODUCTS.map((p) => p.cate_name).filter(Boolean))).sort();
}
